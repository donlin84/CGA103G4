package com.keyword.model;

public class KeyWordVO {
	 private Integer kwid;
	 private String kwName;
	 private String kwContext;
	 
	public Integer getKwid() {
		return kwid;
	}
	public void setKwid(Integer kwid) {
		this.kwid = kwid;
	}
	public String getKwName() {
		return kwName;
	}
	public void setKwName(String kwName) {
		this.kwName = kwName;
	}
	public String getKwContext() {
		return kwContext;
	}
	public void setKwContext(String kwContext) {
		this.kwContext = kwContext;
	}
	 
	 
}
